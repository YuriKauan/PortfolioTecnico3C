    public void compraFichas(int valor_ficha) {
        valor_entrada = Integer.parseInt(campo_valor_entrada.getText());
        troco = valor_entrada - fichas_compradas;
        
        if (troco >= valor_ficha){
            fichas_compradas = fichas_compradas + valor_ficha;
            troco = troco - valor_ficha;
            jorgesLabel.setText("J$ " + Integer.toString(fichas_compradas));
            trocoLabel.setText("R$ " + Integer.toString(troco));
        } 
        else {
            JOptionPane.showMessageDialog(null, "Valor de entrada insuficiente para comprar essa ficha.");
        }
    }

