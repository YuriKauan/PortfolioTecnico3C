    private void botaoJogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoJogarActionPerformed
        if(fichas_compradas >= 10){
            if (troco > 0){
                JOptionPane.showMessageDialog(null, "Você recebeu R$" + troco + " de troco!");
            }
            JOptionPane.showMessageDialog(null, "Divirta-se!");
            MenuJogos frame = new MenuJogos();
            frame.iniciaFichas(fichas_compradas);
            frame.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "Fichas insuficientes! (São necessários 10 jorges para jogar)");
        }
    }//GEN-LAST:event_botaoJogarActionPerformed

